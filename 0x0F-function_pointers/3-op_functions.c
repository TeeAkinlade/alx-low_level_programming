#include "3.calc.h"
/**
 * op_add - calculate the sum of two integers
 * @a: first integer
 * @b: second integer
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - calculate the difference btw two integer
 * @a: first integer
 * @b: second integer
 * Return: substract two integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply two integers
 * @a: first integer
 * @b: second integer
 * Return: multiply two integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides two integer
 * @a: first integer
 * @b: second integer
 * Return: returns the value
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - reminder of two integers
 * @a: first integer
 * @b: second integer
 * Return: returns reminder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
